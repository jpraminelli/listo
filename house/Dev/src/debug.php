<?php

function p($var)
{
    echo "<pre>\n";
    echo "========================================================================================================================\n";
    echo trim(print_r($var, true), "\n") . "\n";
    echo "========================================================================================================================\n";
    echo "</pre>\n";
}

function pe($var)
{
    p($var);
    die;
}

function vd($var)
{
    echo "<pre>\n";
    echo "========================================================================================================================\n";
    echo trim(var_dump($var), "\n"); // . "\n";
    echo "========================================================================================================================\n";
    echo "</pre>\n";
    die;
}
